function choose(arr, recent=[]){
  if(!Array.isArray(arr) || !arr.length) return '';
  const avoided = arr.filter(x=>!recent.some(r=>typeof r==='string' && r.includes(String(x).slice(0,18))));
  const pool = avoided.length ? avoided : arr;
  return pool[Math.floor(Math.random()*pool.length)];
}

function safeTemplateValue(value, key=''){
  if(value == null) return '';
  if(typeof value === 'string' || typeof value === 'number' || typeof value === 'boolean') return String(value);
  if(Array.isArray(value)) return value.map(item=>safeTemplateValue(item,key)).filter(Boolean).join('、');
  if(typeof value === 'object'){
    if(value.name != null) return String(value.name);
    if(value.text != null) return String(value.text);
    if(value.label != null) return String(value.label);
    if(value.joker) return '🃏ババブタ';
    if(value.rank != null){
      const suitIcons={apple:'🍎',corn:'🌽',cabbage:'🥬',mud:'💧'};
      return `${value.rank}${suitIcons[value.suit] ? ` ${suitIcons[value.suit]}` : ''}`;
    }
    return ['card','drawn'].includes(key) ? 'この札' : '';
  }
  return String(value);
}

function render(template, ctx){
  const safe = Object.assign({
    target:'相手',winner:'勝者',weakest:'最弱',speaker:'CPU',card:'この札',drawn:'この札',
    round:1,remaining:'数',penalty:20,mode:'現在の失点方式'
  }, ctx || {});
  return String(template || '').replace(/\{(\w+)\}/g, (_,key)=>safeTemplateValue(safe[key],key));
}

function cleanLine(text){
  return String(text || '')
    .replace(/\s+/g,' ')
    .replace(/。{2,}/g,'。')
    .replace(/！{2,}/g,'！')
    .trim();
}

function createPersonaLine(characterKey, eventType, ctx={}, recent=[]){
  const persona = DIALOGUE_POOLS[characterKey];
  if(!persona) return null;
  const type = COMMON_EVENTS.includes(eventType) ? eventType : 'normal';
  const eventPool = persona.events[type] || persona.events.normal;
  if(!eventPool?.length) return null;

  let core = render(choose(eventPool, recent), ctx);
  const usePrefix = Math.random() < .22 && !core.startsWith(String(ctx.target || '相手'));
  const useTag = Math.random() < .24;
  const prefix = usePrefix ? render(choose(persona.prefixes, recent), ctx) : '';
  const tag = useTag ? render(choose(persona.tags, recent), ctx) : '';

  let line = cleanLine(`${prefix}${core}${tag ? ` ${tag}` : ''}`);
  // Keep normal rail dialogue readable. The central spotlight can use the same
  // complete sentence and scale its font, but the line itself should remain concise.
  if([...line].length > 86){
    line = cleanLine(`${prefix}${core}`);
  }
  return line;
}

function getSpotlightPresentation(characterKey, eventType){
  const base = SPOTLIGHT_PRESENTATION[eventType] || SPOTLIGHT_PRESENTATION.normal;
  let emotion = base.emotion;
  // Character-specific emotional defaults make the supplied portraits feel natural.
  if(characterKey === 'wakumodoki' && emotion === 'pleased') emotion = 'joy';
  if(characterKey === 'kamomodoki' && eventType === 'trickWeak') emotion = 'anger';
  if(characterKey === 'rikumodoki' && ['offSuit','closeCall'].includes(eventType)) emotion = 'sad';
  const ext = characterKey === 'kamomodoki' ? '.jpg' : '.png';
  const imageStem = emotion === 'joy' ? 'joy' : emotion === 'anger' ? 'anger' : emotion === 'sad' ? 'sad' : emotion === 'pleased' ? 'pleased' : 'normal';
  return Object.freeze({
    emotion,
    imageFile:`${imageStem}${ext}`,
    bubble:base.bubble,
    animation:base.animation,
    priority:base.priority
  });
}

